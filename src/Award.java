public class Award {
    private String name;
    private boolean staus;

    public Award() {
        this.name  = "nane";
        this.staus = false;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isStaus() {
        return staus;
    }

    public void setStaus(boolean staus) {
        this.staus = staus;
    }
}


